using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Ejer.Calc
{
    class Operaciones
    {
        public double Suma (double a, double b)
        {
            return a + b;
        }

        public double Resta (double a, double b)
        {
            return a - b;
        }

        public double Mul (double a, double b)
        {
            return a * b;
        }

        public double Div (double a, double b)
        {
            return a / b;
        }

    }
}
